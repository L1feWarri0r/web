       <h2>Frequently Asked Questions (FAQs)</h2>
       <h4>1. How does one apply?</h4>
       <p>The admission forms are normally available in the month of February onwards. The admission advertisements appear in the National and Regional Newspapers where all the details are given. You could get the form by sending Rs. 800/- by demand draft to The Secretary, Banasthali Vidyapith or paying cash Rs. 800/- in person and send the duly filled form by the Last Date given in the Admission Notice.
       <br> Online Submission of application is also possible at the Banasthali Vidyapith's website.
        For detail please see the ADMISSIONS link of the website.</p>
<h4>2. On whose name the payments are to be made?</h4>
<p>The payments are not accepted by cheque. The draft can be drawn on any bank in favour of "Banasthali Vidyapith" payable at  Banasthali Vidyapith / Jaipur</p>
<h4>3. Is Banasthali only for women?</h4>
<p>Banasthali Vidyapith is only for women providing education from Nursery to Ph.D. level.</p>
       <h4>4. Is Banasthali Vidyapith residential?</h4>
       <p>It is a fully residential institution. The admission in hostel starts from class VI onwards.</p>
       <h4>5. What is the medium of instruction?</h4>
       <p>Medium of instruction in school education is mainly Hindi. However we cater to the need of students who want to study in English medium by providing bilingual system - Instructions, Books and Question Papers are in English as well as Hindi. In Higher Education both English and Hindi are used as medium of instruction.</p>
       <h4>6. Does Banasthali offer distance learning program?</h4>
       <p>Banasthali Vidyapith does not offer any program under distance learning.</p>
       <h4>7. Does Banasthali Vidyapith has any other campus?</h4>
       <p>The  Banasthali Vidyapith presently not offering any other campus.</p>
       <h4>	
        8. Does Banasthali Vidyapith provide Scholarship?</h4>
        <p>Yes, there are many schemes helping students financially such as scholarships, for details please see Financial Assistance.</p>
        <h4>9. Can I study courses in Dance/Horse Riding/Gliding & Flying?</h4>
        <p>Yes, you can if only you are a full time student of a regular course of Banasthali Vidyapith. All part time courses are open only to full time students of Banasthali Vidyapith.</p>
