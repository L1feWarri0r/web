       <h2>About Library</h2>
<p>The Banasthali Vidyapith library came into existence in 1935; till 1939 the library procured 1700 books and a Reading Room for students. "Veerbala" a quarterly journal was also brought out for students to improve their creative ability and writing. The library collection grew to 5000 and 12,000 books in 1945 and 1955 respectively. The primary mission of the library is to support the educational and research programme in the Vidyapith by maximizing access to information. In order to fulfill the objectives of the Vidyapith, the library aims to develop a comprehensive collection of documents useful for the faculty and the research community. This rich and valuable collection built over eight decades has some of the rare reference materials and several important journals.</p>
<p>Since then there is consistent growth of the collection of the library. Today, the Libraries' collection exceeds 450000 records including books, reports, theses, dissertations, bound journals, manuscripts, albums, maps, charts, audio/video cassettes, gramophone records, CDs/DVDs, atlas, slides and many more. The library currently receives over 500 national and international print journals. In addition, so many e-journals, e-databases and e-books are subscribed by the libraries. The access of eShodhSindhu is also available. The library offers Reference Services, Users' Orientation Services, Circulation Services, Current Awareness Services, and Plagiarism Checking etc.<br><br>

In addition to Central Library there are 10 departmental libraries in higher education system and 3 libraries in school division.</p>
